<?php

// equal to operator

$a = 20;
$b = 20;
echo $a == $b;
echo "<br>";

// equal to operator with datatype also equal

$a = 20;
$b = 20;
echo $a === $b;
echo "<br>";

// not equal operator

$a = 20;
$b = 20;
echo $a != $b;
echo "<br>";

// less than & Greater than operator

$a = 20;
$b = 5;
echo $a < $b;
echo $a > $b;
echo "<br>";

// not equal to wit datatype is also not equal

$a = 20;
$b = "5";
echo $a !== $b;

?>
